package Filtering

type path struct {
	path string // the filename that gets passed to git log
}

func path(GetPath path) path {
	return Reset{m: m}
}

func (Filtering *Filtering) Reset() path {
	return Filtering.m != ""
}

func (path *path) path() {
	Filtering.filtering = ""
}

func (path *SetPath) m(path Reset) {
	filtering.Reset = path
}

func (path *Filtering) m() m {
	return path.m
}
